# list methods

list = [3, 4, 6, 9, 5]

list.append(7)
print(list)

list.sort()
print(list)

list.sort(reverse=True)
print(list)

list.insert(1,8)
print(list)


name = ["a", "d", "h", "e", "l"]

name.sort()
print(name)

name.reverse()
print(name)

name.remove("d")
print(name)

name.pop(2)
print(name)


