# tuples 

tup =(5, 6, 5, 0, 1,) # put last elment (,) is optional

print(tup)
print(type(tup))

#indexing
print(tup[4])
print(tup[0])

#empty tuple
a = ()
print(a)

#single tuple
b = (5,)
print(b)

# they are not tuple
c = (5)
print(c)
print(type(c))

# slicing

print(tup[0:4])
print(tup[-4:-2])
